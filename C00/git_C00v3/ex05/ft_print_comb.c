#include<unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	exception(char x, char y, char z)
{
	if (x != '7' || y != '8' || z != '9')
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				ft_putchar(a);
				ft_putchar(b);
				ft_putchar(c);
				exception(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}

